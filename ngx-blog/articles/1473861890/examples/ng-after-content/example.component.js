import * as ngCore from '@angular/core';

import { childComponent } from './child.component';
import { titleComponent } from './title.component';
import { itemComponent } from './item.component';

function _ngAfterContentComponent (){
  this.constructor = function ngAfterContentComponent(){};

  this.ngOnInit = function() {
    this.title = 'ngAfterContent';
    this.items = [
      { id: 1, name: 'item 01' },
      { id: 2, name: 'item 02' }
    ];
  };
}

export var ngAfterContentComponent = ngCore.Component({
  selector: 'my-example',
  template: [
    '<my-child>',
      '<my-title [title]="title"></my-title>',
      '<my-item *ngFor="let item of items" [item]="item"></my-item>',
    '</my-child>'
  ].join(''),
  directives: [ 
    childComponent,
    titleComponent,
    itemComponent
  ]
})
.Class(new _ngAfterContentComponent());